static const char *getIcon(int layer) {
    switch (layer) {
    case _BASE:
    case _RISE:
    case _LOWR:
    case _CONF:
    case _ADJT:
        return icons[layer];
    default:
        return icons[_BASE];
    }
};

static void draw_logo(void) {
    oled_set_cursor(0, 0);
    oled_write_raw_P(getIcon(get_highest_layer(layer_state)), 160);
    oled_set_cursor(0, 5);
}

static void print_slave(void) {
    draw_logo();

    /* caps lock */
    // oled_set_cursor(0, 8 );
    // if (led_usb_state.caps_lock) {
    //     oled_write("CPSLK", false);
    // } else {
    //     oled_write("     ", false);
    // }
    oled_write("CPSLK", led_usb_state.caps_lock);
}

static void print_master(void) {
    draw_logo();

    oled_write_ln_P(get_layer_name(get_highest_layer(layer_state)), false);
    oled_write_ln_P(PSTR("\n"), false);
}

oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    return OLED_ROTATION_270;
}

bool oled_task_user(void) {
    led_usb_state = host_keyboard_led_state();

    if (is_keyboard_master()) {
        print_master();
    } else {
        print_slave();
    }

    return false;
}

